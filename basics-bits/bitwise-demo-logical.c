#include <stdio.h>

int main()  {
    short int w1 = 25, w2 = 77, w3 = 0;
    w3 = w1 & w2; // & 1 <> 1 ? 1 : 0
    // 0001 1001
    // 1001 1101
    // ---------
    // 0000 1001 = 9
    printf("%d \n", w3);

    short int x1 = 147, x2 = 61, x3 = 0;
    x3 = x1 | x2; // | ( 1 <> 1 || 1 <> 0 || 0 <> 1 ) ? 1 : 0
    // 1001 0011
    // 0011 1101
    // ---------
    // 1011 1111 = 197
    printf("%d \n", x3);

    short int y1 = 147, y2 = 61, y3 = 0;
    y3 = y1 ^ y2; // ^ ( 1 <> 0 || 0 <> 1 ) ? 1 : 0     //// 1 <> 1 = 0   0 <> 0 = 0
    // 1001 0011
    // 0011 1101
    // ---------
    // 1010 1110 = 174
    printf("%d \n", y3);

    // swap #1
    y3 = y1; y1 = y2; y2 = y3;
    // swap #2
    y1 ^= y2; y2 ^= y1; y1 ^= y2;

    signed int z1 = 2, z3 = 0;
    z3 = ~(z1); // unary // ^ 1 -> 0 || 0 -> 1 
    // 0000 0010
    // ---------
    // 1111 1101 = 174
    printf("%d \n", z3);

    z1 = 154, z3 = 0;
    z3 = ~(z1); // unary // ^ 1 -> 0 || 0 -> 1 
    // 1001 1010
    // ---------
    // 0110 0101 = -155
    printf("%d \n", z3);

    return 0;
}


